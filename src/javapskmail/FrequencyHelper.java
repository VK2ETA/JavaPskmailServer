    /**
     * Here is the method that calculates the dial fq
     */
    public void Calculatedial(){
        Float Serverfq;
        Float Audiofq;
        Float Dialfq;
        Serverfq = Float.parseFloat(this.cboServerfq.getSelectedItem().toString());
        Audiofq = Float.parseFloat(this.cboCenterLF.getSelectedItem().toString());
        if (this.radUSB.isSelected()){
            Dialfq = Serverfq-(Audiofq/1000);
        }
        else
        {
            Dialfq = Serverfq+(Audiofq/1000);
        }
        this.lblDialFq.setText(Dialfq.toString());
    }

